"use strict";require("dotenv").config();
const {BigQuery}=require("@google-cloud/bigquery"),fs2=require("fs");
const P=process.env.GCP_PROJECT_ID||"savvy-gtm-analytics";
const V=P+".Tableau_Views.vw_funnel_master";
const bt=String.fromCharCode(96),T=bt+V+bt;
const bq=new BigQuery({projectId:P,keyFilename:process.env.GOOGLE_APPLICATION_CREDENTIALS});
async function runQ(l,s){try{const[r]=await bq.query({query:s,useLegacySql:false});return r;}catch(e){console.error("ERR",l,e.message);return null;}}
async function main(){
var sq=String.fromCharCode(39),lp=String.fromCharCode(40),rp=String.fromCharCode(41),cm=String.fromCharCode(44);
var FN_C="COALESCE",FN_AQ="APPROX_QUANTILES",FN_AVG="AVG",FN_SV="SAFE_DIVIDE",FN_SD="STDDEV",FN_DF="DATE_DIFF",FN_D="DATE",FN_TS="TIMESTAMP",FN_CD="CURRENT_DATE",FN_CI="COUNTIF",FN_CNT="COUNT";
var C1="Underwritten_AUM__c",C2="Amount";
var AUM=FN_C+lp+C1+cm+C2+rp;
var aq=p=>FN_AQ+lp+AUM+",100"+rp+"[OFFSET"+lp+p+rp+"]/1e6";
var aqF=function(f,p){return FN_AQ+lp+f+",100"+rp+"[OFFSET"+lp+p+rp+"]/1e6";};
var R={};
var SY=sq+"Yes"+sq;
var SJ=sq+"Joined"+sq,SCL=sq+"Closed Lost"+sq;
var BASE="Full_Opportunity_ID__c IS NOT NULL AND is_primary_opp_record=1";
var SQO_F=" AND SQO_raw="+SY;
var STAGE_F=" AND StageName IN "+lp+SJ+cm+SCL+rp;
var AUM_POS=" AND "+AUM+">0";
var WF=BASE+SQO_F+STAGE_F;
var WF_AUM=WF+AUM_POS;
var WF_J=BASE+SQO_F+" AND "+lp+"StageName="+SJ+" OR advisor_join_date__c IS NOT NULL"+rp+" AND StageName!="+SCL+AUM_POS;
var QSEL=FN_CNT+lp+"*"+rp+" AS n,"+aq(25)+" AS p25_M,"+aq(50)+" AS median_M,"+FN_AVG+lp+AUM+rp+"/1e6 AS mean_M,"+aq(75)+" AS p75_M";
var DFN=FN_D+lp+"Opp_CreatedDate"+rp;
var DS=function(n){return "DATE_SUB"+lp+FN_CD+lp+rp+",INTERVAL "+n+" DAY"+rp;};
var W180=" AND "+DFN+">="+DS(180);
var W1yr=" AND "+DFN+">="+DS(365);
var W2yr=" AND "+DFN+">="+DS(730);
var SEL=function(wl,wf){return "SELECT "+sq+wl+sq+" AS window_label,"+QSEL+" FROM "+T+" WHERE "+wf;};
var q1at=SEL("all_time",WF_AUM);
var q1_180=SEL("180d",WF_AUM+W180);
var q1_1yr=SEL("1yr",WF_AUM+W1yr);
var q1_2yr=SEL("2yr",WF_AUM+W2yr);
var r1=[],r1r=await runQ("Q1-180d",q1_180);
if(r1r)r1=r1.concat(r1r);
r1r=await runQ("Q1-1yr",q1_1yr);
if(r1r)r1=r1.concat(r1r);
r1r=await runQ("Q1-2yr",q1_2yr);
if(r1r)r1=r1.concat(r1r);
r1r=await runQ("Q1-all_time",q1at);
if(r1r)r1=r1.concat(r1r);
R.q1=r1;
console.log("Q1 Resolved SQO AUM:");
r1.forEach(r=>console.log(JSON.stringify(r)));
var q2at=SEL("all_time",WF_J);
var q2_180=SEL("180d",WF_J+W180);
var q2_1yr=SEL("1yr",WF_J+W1yr);
var q2_2yr=SEL("2yr",WF_J+W2yr);
var r2=[],r2r=await runQ("Q2-180d",q2_180);
if(r2r)r2=r2.concat(r2r);
r2r=await runQ("Q2-1yr",q2_1yr);
if(r2r)r2=r2.concat(r2r);
r2r=await runQ("Q2-2yr",q2_2yr);
if(r2r)r2=r2.concat(r2r);
r2r=await runQ("Q2-all_time",q2at);
if(r2r)r2=r2.concat(r2r);
R.q2=r2;
console.log("Q2 Joined SQO AUM:");
r2.forEach(r=>console.log(JSON.stringify(r)));
var ESP=FN_C+lp+"Stage_Entered_Sales_Process__c,Stage_Entered_Negotiating__c,Stage_Entered_Signed__c,Stage_Entered_Joined__c"+rp;
var ENEG=FN_C+lp+"Stage_Entered_Negotiating__c,Stage_Entered_Signed__c,Stage_Entered_Joined__c"+rp;
var ESIG=FN_C+lp+"Stage_Entered_Signed__c,Stage_Entered_Joined__c"+rp;
var EJOIN=FN_C+lp+"Stage_Entered_Joined__c,"+FN_TS+lp+"advisor_join_date__c"+rp+rp;
var dd=function(t1,t2){return FN_DF+lp+FN_D+lp+t2+rp+","+FN_D+lp+t1+rp+",DAY"+rp;};
var CW="CASE WHEN";
var avgdd=function(t1,t2,a1,a2){var chk=CW+" "+t1+" IS NOT NULL AND "+t2+" IS NOT NULL AND "+FN_D+lp+t1+rp+"<="+FN_D+lp+t2+rp;return "ROUND"+lp+FN_AVG+lp+chk+" THEN "+dd(t1,t2)+" END"+rp+",1"+rp+" AS avg_"+a1+"_to_"+a2;};
var SQOB="Date_Became_SQO__c";
var QVEL=FN_CNT+lp+SQOB+rp+" AS n,"+avgdd(SQOB,ESP,"sqo","sp")+","+avgdd(ESP,ENEG,"sp","neg")+","+avgdd(ENEG,ESIG,"neg","signed")+","+avgdd(ESIG,EJOIN,"signed","joined");
QVEL+=","+avgdd(SQOB,EJOIN,"sqo","joined_direct");
var WF_VEL=BASE+SQO_F+" AND StageName="+SJ+" AND "+SQOB+" IS NOT NULL";
var SELV=function(wl,wf){return "SELECT "+sq+wl+sq+" AS window_label,"+QVEL+" FROM "+T+" WHERE "+wf;};
var q3at=SELV("all_time",WF_VEL);
var q3_180=SELV("180d",WF_VEL+W180);
var q3_1yr=SELV("1yr",WF_VEL+W1yr);
var q3_2yr=SELV("2yr",WF_VEL+W2yr);
var r3=[],r3r;
r3r=await runQ("Q3-180d",q3_180);if(r3r)r3=r3.concat(r3r);
r3r=await runQ("Q3-1yr",q3_1yr);if(r3r)r3=r3.concat(r3r);
r3r=await runQ("Q3-2yr",q3_2yr);if(r3r)r3=r3.concat(r3r);
r3r=await runQ("Q3-all_time",q3at);if(r3r)r3=r3.concat(r3r);
R.q3=r3;
console.log("Q3 SQO Velocity:");
r3.forEach(r=>console.log(JSON.stringify(r)));
var CIIF=function(expr){return FN_CI+lp+expr+rp;};
var AUM_NULL=AUM+" IS NULL";
var AUM_ZERO=AUM+"=0";
var AUM_NOZ=AUM_NULL+" OR "+AUM_ZERO;
var UW="Underwritten_AUM__c",AM="Amount";
var QPREV=FN_CNT+lp+"*"+rp+" AS total"+","+CIIF(UW+" IS NULL")+" AS null_uw"+","+CIIF(AM+" IS NULL")+" AS null_amt"+","+CIIF(AUM_NULL)+" AS null_aum"+","+CIIF(AUM_ZERO)+" AS zero_aum"+","+CIIF(AUM_NOZ)+" AS null_or_zero"+","+FN_SV+lp+CIIF(AUM_NOZ)+","+FN_CNT+lp+"*"+rp+rp+" AS null_or_zero_rate"+","+CIIF(UW+" IS NOT NULL")+" AS has_uw"+","+FN_SV+lp+CIIF(UW+" IS NOT NULL")+","+FN_CNT+lp+"*"+rp+rp+" AS uw_rate";
var q4="SELECT "+QPREV+" FROM "+T+" WHERE "+WF;
var r4=await runQ("Q4-null_zero_prevalence",q4);
R.q4=r4;
console.log("Q4 Null/Zero AUM Prevalence:");
if(r4)r4.forEach(r=>console.log(JSON.stringify(r)));
var QDIST=FN_CNT+lp+"*"+rp+" AS n"+","+aqF(AUM,10)+" AS p10_M"+","+aqF(AUM,25)+" AS p25_M"+","+aqF(AUM,50)+" AS p50_M"+","+FN_AVG+lp+AUM+rp+"/1e6 AS mean_M"+","+aqF(AUM,75)+" AS p75_M"+","+aqF(AUM,90)+" AS p90_M"+","+aqF(AUM,95)+" AS p95_M";
QDIST+=",MAX"+lp+AUM+rp+"/1e6 AS max_M"+","+FN_SD+lp+AUM+rp+"/1e6 AS stddev_M";
QDIST+=","+FN_SV+lp+FN_AVG+lp+AUM+rp+"-"+FN_AQ+lp+AUM+",100"+rp+"[OFFSET"+lp+50+rp+"]"+","+FN_SD+lp+AUM+rp+rp+" AS pearson_skew";
QDIST+=","+CIIF(AUM+"<25e6")+" AS t1_u25M"+","+CIIF(AUM+">=25e6 AND "+AUM+"<75e6")+" AS t2_25_75M"+","+CIIF(AUM+">=75e6 AND "+AUM+"<150e6")+" AS t3_75_150M"+","+CIIF(AUM+">=150e6")+" AS t4_o150M";
var q5at="SELECT "+sq+"all_time"+sq+" AS window_label,"+sq+"ALL"+sq+" AS StageName,"+QDIST+" FROM "+T+" WHERE "+WF_AUM;
var q5j="SELECT "+sq+"all_time"+sq+" AS window_label,"+sq+"Joined"+sq+" AS StageName,"+QDIST+" FROM "+T+" WHERE "+WF_AUM+" AND StageName="+SJ;
var q5cl="SELECT "+sq+"all_time"+sq+" AS window_label,"+sq+"Closed Lost"+sq+" AS StageName,"+QDIST+" FROM "+T+" WHERE "+WF_AUM+" AND StageName="+SCL;
var r5=[],r5r;
r5r=await runQ("Q5-all",q5at);if(r5r)r5=r5.concat(r5r);
r5r=await runQ("Q5-joined",q5j);if(r5r)r5=r5.concat(r5r);
r5r=await runQ("Q5-closed_lost",q5cl);if(r5r)r5=r5.concat(r5r);
R.q5=r5;
console.log("Q5 AUM Distribution:");
r5.forEach(r=>console.log(JSON.stringify(r)));
fs2.writeFileSync("C:/Users/russe/documents/dashboard/bq-aum-results.json",JSON.stringify(R,null,2));
console.log("Results saved to bq-aum-results.json");
}
main().catch(e=>{console.error("FATAL:",e.message);process.exit(1);});
